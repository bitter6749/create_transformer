// ?????????: W = W - lr * dW
void gradient_descent_update(Matrix *W, const Matrix *dW, float lr) { 
  int total_elements = W->rows * W->cols;
  for (int i = 0; i < total_elements; i++) {
    W->data[i] -= lr * dW->data[i];
  }
}

